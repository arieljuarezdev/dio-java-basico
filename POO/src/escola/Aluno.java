package escola;

public class Aluno {
    private String nome;
    private int idade;

    public String getNome(){
        return nome;
    }

    public void setNome(String newName){
        this.nome = newName;
    }

    public int getIdade(){
        return idade;
    }

    public void setIdade(int newIdade){
        this.idade = newIdade;
    }

}
